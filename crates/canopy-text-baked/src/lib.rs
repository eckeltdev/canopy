//! Canopy baked-font text backend: a small, dependency-free 8x8 monospace bitmap
//! font covering the printable ASCII range `0x20..=0x7E`.
//!
//! This is the glyph source for constrained / bare-metal renderers that cannot
//! carry Parley or a TrueType rasterizer. A software [`canopy_traits::Renderer`]
//! blits these glyphs directly into a pixel buffer. The table is a plain `const`,
//! so it lives in flash on an MCU and costs nothing at startup.
//!
//! ## Bit layout
//! [`glyph`] returns 8 bytes — one per pixel row, top to bottom. Within a row,
//! **bit 7 (`0x80`) is the leftmost pixel** and bit 0 (`0x01`) the rightmost. A set
//! bit means "ink" (paint the foreground color); a clear bit means "leave the
//! background showing through".
//!
//! ```
//! use canopy_text_baked::{glyph, CELL_W, CELL_H};
//! assert_eq!((CELL_W, CELL_H), (8, 8));
//! // The space glyph is entirely blank.
//! assert_eq!(glyph(' '), [0; 8]);
//! ```
#![cfg_attr(not(test), no_std)]

extern crate alloc;

/// Glyph cell width in pixels (advance width of the monospace cell).
pub const CELL_W: u32 = 8;
/// Glyph cell height in pixels.
pub const CELL_H: u32 = 8;

/// First code point covered by the baked table (`' '`).
const FIRST: u32 = 0x20;
/// Last code point covered by the baked table (`'~'`).
const LAST: u32 = 0x7E;

/// The fallback glyph for code points outside the baked range: a filled box, so
/// unsupported text is visibly "tofu" rather than silently invisible.
const TOFU: [u8; 8] = [
    0b0000_0000,
    0b0111_1110,
    0b0111_1110,
    0b0111_1110,
    0b0111_1110,
    0b0111_1110,
    0b0111_1110,
    0b0000_0000,
];

/// The 8x8 bitmap for one character.
///
/// Returns 8 rows (top to bottom); within a row, bit `0x80` is the leftmost pixel
/// and a set bit is "ink". Characters below `0x20` or above `0x7E` return a filled
/// box ([`TOFU`]) so unsupported glyphs are visible.
pub fn glyph(c: char) -> [u8; 8] {
    let code = c as u32;
    if (FIRST..=LAST).contains(&code) {
        FONT[(code - FIRST) as usize]
    } else {
        TOFU
    }
}

