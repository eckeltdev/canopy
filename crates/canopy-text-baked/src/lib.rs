#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn cell_dimensions() {
        assert_eq!((CELL_W, CELL_H), (8, 8));
    }

    #[test]
    fn space_is_blank() {
        assert_eq!(glyph(' '), [0; 8]);
    }

    #[test]
    fn capital_a_bitmap() {
        // A pointed roof, a crossbar, and two legs — the classic 8x8 'A'.
        assert_eq!(
            glyph('A'),
            [
                0b0011_1000,
                0b0110_1100,
                0b1100_0110,
                0b1100_0110,
                0b1111_1110,
                0b1100_0110,
                0b1100_0110,
                0b0000_0000,
            ]
        );
    }

    #[test]
    fn colon_has_two_dots_and_gaps() {
        let g = glyph(':');
        // Two ink rows (the dots) and the rest clear.
        let ink_rows: usize = g.iter().filter(|&&r| r != 0).count();
        assert_eq!(ink_rows, 4); // each dot is 2px tall
        assert_eq!(g[0], 0x00, "top row blank");
        assert_eq!(g[3], 0x00, "middle gap blank");
        assert_eq!(g[7], 0x00, "bottom row blank");
    }

    #[test]
    fn digit_zero_is_a_ring() {
        let g = glyph('0');
        // Top and bottom are closed; the middle rows have a hollow interior.
        assert_eq!(g[0], 0x7C);
        assert_eq!(g[6], 0x7C);
        // An interior row has ink on both edges but a gap in the middle.
        assert_ne!(g[3] & 0x80, 0, "left wall");
    }

    #[test]
    fn unknown_below_space_is_tofu() {
        assert_eq!(glyph('\u{0007}'), TOFU);
        assert_ne!(glyph('\u{0007}'), [0; 8]);
    }

    #[test]
    fn unknown_above_tilde_is_tofu() {
        assert_eq!(glyph('\u{00E9}'), TOFU); // 'é'
    }

    #[test]
    fn every_letter_and_digit_has_ink() {
        for c in ('A'..='Z').chain('a'..='z').chain('0'..='9') {
            let g = glyph(c);
            assert!(g.iter().any(|&r| r != 0), "{c} should not be blank");
        }
    }
}
